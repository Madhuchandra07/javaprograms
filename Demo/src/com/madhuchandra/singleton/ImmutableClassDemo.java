package com.madhuchandra.singleton;

public final class ImmutableClassDemo {
	final int id;
	final String name;

	public ImmutableClassDemo(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}






}
